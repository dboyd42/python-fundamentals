# Program: Distance Traveled
# Description: This program displays the distance a car will travel in 6, 10,
#              15 hours at 70mph.

# Declare variables
speed = 70
time1 = 6
time2 = 10
time3 = 15

# Calculate Distances
distance1 = (speed * time1)
distance2 = (speed * time2)
distance3 = (speed * time3)

# Display results
print('The distance the car wil travel in', time1, 'hours is', distance1)
print('The distance the car wil travel in', time2, 'hours is', distance2)
print('The distance the car wil travel in', time3, 'hours is', distance3)
